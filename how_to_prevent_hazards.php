                    <h3>HOW TO PREVENT ENVIRONMENTAL HAZARDS</h3>
                    <div class="details">
                        <span class="category" style="color:#fff; background-color:red">Environmental hazards</span>
                        <span><strong>Pubished: </strong>30<sup>th</sup> October, 2020</span>
                    </div>
                    <hr>
                    <p>Workplace injuries due to electrical hazards are more than a possibility; they are a reality in many areas. There are thousands of people injured or worse every year. Often, preventing injuries is as simple as recognizing the possible hazards and taking appropriate measures. Making workers aware of the potential electrical hazards at their place of business goes a long way to reducing injury and other problems. Fortunately, there are only a few things to remember to prevent most of the problems and avoid potential hazards.<br><br>
                    <ol>
                        <li><strong>Turn the power off.</strong> There is one important thing to remember when working with any electrical device. Turn it off. Don’t assume the power is off because the off button has been pressed. Shut down the power at the source and then test to ensure that it is off.</li><br>
                        <li><strong>Inform others.</strong> If there is work on any on electrical equipment, notify everyone in the area. Proper communication helps limit the possibility of reenergizing the equipment while at an inopportune time.</li><br>
                        <li><strong>Lockout/tagout (LOTO).</strong> Ensure that anyone who works on electrical equipment is familiar with the proper lockout and tag out procedures</li><br>
                        <li><strong>Stay away from wires.</strong> Check to see if there are any electrical power lines in the area where an employee is working. Ensure personnel avoids contact with them. If the power lines are visible, make the person aware of their location at all times. If they are not visible, take caution to ensure that they don’t cut into the power lines.</li><br>
                        <li><strong>Maintain equipment.</strong> Using old equipment or equipment that is not well maintained leads to electrical hazards, too. If there is fraying on the wires and equipment, the possibility of electric shock increases</li><br>
                        <li><strong>Wear personal protective equipment (PPE).</strong> Anyone who is working with electrical equipment, or in an area where an electrical shock is possible, needs to wear necessary PPE. Proper PPE includes anything from gloves and footwear to eye and ear protection.</li><br>
                        <li><strong> Avoid arc flash areas.</strong> Avoid areas where arc flash is possible unless the personnel has proper qualifications to work with the equipment.</li><br>
                    </ol>
                    <br><br>   ​   
                    Routine safety training and reminders are imperative to keep all employees safe around electrical equipment. Electrical hazards lead to severe consequences, which is solved with proper training.<br><br> 
                         
                        
