                    <h3>POLICE ARREST 3 BROTHERS FOR THE KIDNAP AND MURDER OF A MOTHER OF 5 IN ABUJA</h3>
                    <div class="details">
                        <span class="category" style="color:#fff; background-color:royalblue">Crime and kidnap</span>
                        <span><strong>Pubished: </strong>20<sup>th</sup> July, 2020</span>
                    </div>
                    <hr>
                    <p>Police operatives attached to the Intelligence Response Unit of the Nigeria Police Force have arrested three (3) suspects namely; Johnson Emmanuel - 38yrs, Gideon Emmanuel - 31yrs and Success Emmanuel - 27yrs for the kidnap and gruesome murder of one Mrs Janet Nnenna Ogbonnaya on 14th May, 2020. The suspects, born by same parents, all male and native of Isiekenesi, Ideato LGA of Imo State were arrested by the Police in their home town where they had taken refuge after committing the crime in Abuja, in order to evade police arrest. <br><br>

                    Investigations that led to the arrest of the suspects is sequel to complaints received from one Chinedu Ogbonnaya who alleged that his mother, Mrs Janet Nnenna Ogbonnaya, 55yrs, native of Ozuitem in Bende LGA of Abia State had been kidnapped and a 5-Million naira ransom demanded before she could be released. A comprehensive and painstaking investigation by the police operatives resulted in the arrest of the three (3) suspects, whom, in the course of interrogation, revealed that the victim had long been murdered and buried. Further findings revealed that the victim, a widow, who had been a Facebook friend of the principal suspect – Johnson Emmanuel, was lured from her home in Gwagwalada to visit the suspect. The suspect thereafter took advantage of the visit, served her yoghurt laced with drugs and subsequently had her murdered.<br> The suspect having killed the victim and buried her remains in a septic tank, went ahead to reach out to the family of the victim using her phone and demanded 5-Million naira ransom as pre-condition for her release.<br><br>   ​
                        
                    Meanwhile, today 4th June, 2020, the suspects led a team of investigators alongside pathologists to a residence at Wumba District, Lokogoma, Abuja where the victim’s decomposing body was exhumed from a septic tank. The exhumed body has been taken to the University Teaching Hospital, Gwagwalada, Abuja for forensic examination.<br><br> 
                        
                    A Toyota Highlander Jeep belonging to the deceased has also been recovered by Police operatives at a mechanic workshop in Apo where it had been repainted into a different colour, vehicle documents fraudulently changed and ownership of the stolen vehicle criminally transferred to one of the masterminds of the crime – Johnson Emmanuel.<br><br> 
                        
                    Investigations also revealed that the house where the deceased was killed and buried originally belonged to one of the suspects but was hurriedly sold-off to a third party apparently to obliterate evidence.<br><br>   
                        
                    The Inspector-General of Police, IGP M.A Adamu, NPM, mni, while commending the operatives for a job well done, reassures that perpetrators of any form of crimes in the country will not go undetected and unpunished. He however enjoins citizens to be more security conscious and report any suspicious activities within their neighbourhood to the nearest police station.<br>
